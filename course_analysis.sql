SELECT * FROM COURSE_ENROLLMENTS;

----TOTALREVENUE-----

SELECT 
  SUM(ENROLLMENTS*PRICE) AS TOTALREVENUE
FROM COURSE_ENROLLMENTS;

-----REVENUE BY COURSE----

SELECT 
  COURSE,
  SUM(ENROLLMENTS*PRICE) AS TOTALREVENUE
FROM COURSE_ENROLLMENTS
GROUP BY COURSE
ORDER BY TOTALREVENUE DESC;

-----REVENUE BY INSTRUCTOR------

SELECT 
  INSTRUCTOR,
  SUM(ENROLLMENTS*PRICE) AS TOTALREVENUE
FROM COURSE_ENROLLMENTS
GROUP BY INSTRUCTOR
ORDER BY TOTALREVENUE DESC;
